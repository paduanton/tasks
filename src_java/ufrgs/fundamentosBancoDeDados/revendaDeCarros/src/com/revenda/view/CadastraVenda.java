    private void carregarDadosModals() throws SQLException {
        listaClientes = clienteDAO.listarTodos();
        DefaultComboBoxModel model = (DefaultComboBoxModel) jComboBox1.getModel();

        model.removeAllElements();

        for (Cliente cliente : listaClientes) {
            model.addElement(cliente.getNome());
        }

        listaVendedores = vendedorDAO.listarTodos();
        DefaultComboBoxModel model2 = (DefaultComboBoxModel) jComboBox2.getModel();

        model2.removeAllElements();

        for (Vendedor vendedor : listaVendedores) {
            model2.addElement(vendedor.getNome());
        }

        listaCarros = carroDAO.listarTodos();
        DefaultComboBoxModel model3 = (DefaultComboBoxModel) jComboBox3.getModel();

        model3.removeAllElements();

        for (Carro carro : listaCarros) {
            model3.addElement(carro.getModelo());
        }
    }

