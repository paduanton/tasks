    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (JOptionPane.showConfirmDialog(this, "VocÃª tem certeza?") == 0) {
            CarroDAO carroDAO = new CarroDAO();
            boolean novo = false;
            int ano = Integer.parseInt(jTextField3.getText()+"");
            if (carro == null) {
                carro = new Carro();
                novo = true;
            }
            carro.setMarca(jTextField2.getText());
            carro.setModelo(jTextField1.getText());
            carro.setAno(ano);
            carro.setValor(BigDecimal.valueOf(Double.parseDouble(jTextField4.getText())));

            try {
                if (novo) {
                    carroDAO.salvar(carro);
                }else{
                    carroDAO.atualizar(carro);
                }
            } catch (SQLException ex) {
                Logger.getLogger(CadastraCarro.class.getName()).log(Level.SEVERE, null, ex);
            }
            this.dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed


