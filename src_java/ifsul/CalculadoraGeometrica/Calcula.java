package br.edu.ifsul.sapucaia.CalculadoraGeometrica;

public class Calcula {
    public double num1;
    public double num2;

    public double soma() {
        return num1 + num2;
    }

    public double sub() {
        return num1 - num2;
    }

    public double mult() {
        return num1 * num2;
    }

    public double div() {
        return num1 / num2;
    }
}
